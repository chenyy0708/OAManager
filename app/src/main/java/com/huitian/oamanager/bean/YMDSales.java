public class YMDSales {

    /**
     * day : 1.41
     * month : 10
     * year : 215
     */

    private double day;
    private int month;
    private int year;

    public double getDay() {
        return day;
    }

    public void setDay(double day) {
        this.day = day;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
